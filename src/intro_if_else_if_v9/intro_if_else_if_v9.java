package intro_if_else_if_v9;

public class intro_if_else_if_v9 {

	public static void main(String[] args) {

		int sayi = 30;
		if (sayi < 15) {
			System.out.println("Sayi 15'den küçüktür.");
		} else if (sayi == 15) {
			System.out.println("Sayi 15'e eşittir.");
		} else {
			System.out.println("Sayi 15'ten büyüktür.");
		}
	}

}